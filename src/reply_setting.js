function catch_content(start_from)
{
	let i=start_from;
	while(message[i]==" "||message[i]=="\r"||message[i]=="\n")
		++i;
	let j=i;
	while(j<message.length&&message[j]!="\n")
		++j;
	if(message[j]=="\n")
		--j;
	return message.substr(i,j-i).replace(/&#91;/g,"[").replace(/&#93;/g,"]").replace(/&amp;/g,"&");
}

