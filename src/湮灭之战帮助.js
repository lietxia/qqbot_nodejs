aw_help_string = `湮灭之战游戏帮助(本游戏由丩卩夂忄创作)
==========
基础操作
aw帮助
·查看此帮助
aw创建,物质名,物质类型
·物质类型：正物质或反物质（可简写为正反或zf）
·物质名：给你的物质起个名
aw查看,物质名
·查看某物质当前的状态
aw删除,物质名
·使【你的】物质消失
aw位置,物质名
·查看某物质的当前位置
aw距离,物质名1,物质名2
·查看两个物质之间的最短距离
aw环境
·查看当前的宇宙环境
aw日志
·查看最近的10条变化
----------
数值操作
aw改变密度.物质名,改变数值
·改变【你的】物质的密度，消耗等同于改变数值的绝对值的行动力，可以增大也可以减小
aw提升攻击力,物质名,提升数值
·提升【你的】物质的攻击力，消耗等同于提升数值的行动力
aw提升防御力,物质名,提升数值
·提升【你的】物质的防御力，消耗等同于提升数值的行动力
aw提升幸运,物质名,提升数值
·提升【你的】物质的幸运，消耗等同于提升数值的行动力
----------
事件操作
aw大灾变,物质名
·消耗1000行动力，使所有其他物质失去80%的质量
aw攻击,物质名,目标物质名,攻击次数
·消耗等同于攻击次数的行动力，使目标物质：
··如果物质类型相同，则使其增加等同于你的攻击力的质量
··如果物质类型不同，则使其减少等同于你的攻击力的质量，目标的防御力会抵消攻击
·两者最短距离越远，命中率越低
·无论命中与否，每次攻击自身减少10质量
==========
玩法介绍
正物质/反物质
·同种物质之间碰撞时，质量较大的物质会抢夺质量较小的物质的质量；不同种物质之间碰撞时，双方均会失去质量
宇宙垃圾
·宇宙垃圾是这个宇宙中会随机生成的小块物质，物质碰撞他们的时候也会有以上变化
瞬移
·所有的东西都是运动的，而这个宇宙里的物质的运动方式是瞬移
·宇宙是有边界的，如果你的物质不慎飞出了宇宙，那么会失去部分质量
·你的物质的球心一定会在宇宙的范围内
攻击/防御
·这个功能还在制作
==========
属性介绍
----------
玩家可更改属性
·密度：决定你的物质的状态【下限1】
·幸运：总感觉会有好事发生【上限5000】
·攻击力：攻击时可以造成的伤害/提供的恢复
·防御力：防御时可以抵消的伤害
·行动力：在进行操作的时候消耗，随着游戏推进会随机获得
----------
玩家不可更改变量
·质量：质量大于1表示物质存在
·体积：随着质量和密度更改
·状态：随密度变化
··1~24=气态
··25~174=液态
··175~999=固态
··1000+=超固态`